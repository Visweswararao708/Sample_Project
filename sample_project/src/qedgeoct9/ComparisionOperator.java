package qedgeoct9;

public class ComparisionOperator {

	public static void main(String[] args) {
int x=200,y=300,z=200;
System.out.println(x==y);
System.out.println(x==z);
System.out.println(x!=y);
System.out.println(y>x);
System.out.println(x<y);
System.out.println(x>=z);
System.out.println(y<=x);
System.out.println(x<=z);
	}

}
